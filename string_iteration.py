
# Task: Write a Python script that demonstrates iterating over a string using a for loop.
# Count the characters in the string and print each character followed by an asterisk.

#LOOPS
#CONCEPT IN PRACTICE No. 3

str_var = "A string"

count = 0
for c in str_var:
    count += 1
print(c, end='*')

