
# Task: Write a Python script that uses a for loop to count down from 5 to 1 and then print a completion message.

for i in range(5, 0, -1):
    print(i)
print("Done with loop!")

# Explanation: This script demonstrates the use of a for loop with a range that counts down.
# It prints numbers from 5 down to 1 and then prints a final message.
# This example teaches loop control, range usage, and basic output in Python.
